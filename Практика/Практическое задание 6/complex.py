a = complex(3, 4)
b = complex(6, 4)
print('Комплексное число a:', a)
print('Комплексное число b:', b)
c = a + b
print('Сложения комплексных чисел:', c)
print('Мнимая часть комплексного числа а:', a.imag)
print('Действительная часть комплексного числа b:', b.real)
print('Проверим на равенство комплексные числа a и b:', a == b)
print('Модуль комплексного числа a:', abs(a))
print('Возведение в четвертую степень комплексного числа b', pow(b, 4))
